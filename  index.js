// Helper function to calculate the square of a number
function square(number) {
    return number * number;
  }
  
  // Helper function to calculate the sum of two numbers
  function sum(a, b) {
    return a + b;
  }
  
  // Helper function to calculate the difference between two numbers
  function difference(a, b) {
    return a - b;
  }
  
  // Helper function to calculate the product of two numbers
  function product(a, b) {
    return a * b;
  }
  
  // Example usage of helper functions
  console.log(square(5)); // Output: 25
  console.log(sum(3, 4)); // Output: 7
  console.log(difference(8, 3)); // Output: 5
  console.log(product(2, 6)); // Output: 12
  